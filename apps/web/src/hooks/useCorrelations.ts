'use client';

/**
 * @module useCorrelations
 *
 * React hook for fetching and filtering correlation data from the
 * correlation_cache Supabase table. Falls back to realistic mock data when
 * the table is empty or unavailable (cold-start / demo scenario).
 *
 * The hook applies client-side filtering and sorts results by a combined
 * relevance score of confidence * abs(coefficient).
 */

import { useState, useEffect, useMemo } from 'react';
import { createClient } from '@/lib/supabase/client';

// ─────────────────────────────────────────────────────────────────────────────
// Public types
// ─────────────────────────────────────────────────────────────────────────────

export interface CorrelationFilters {
  /** Only include correlations involving these dimensions (empty = all). */
  dimensions?: string[];
  /** Minimum confidence score in [0, 1]. Default 0.5. */
  minConfidence?: number;
  /** Date range for the analysis window. */
  timePeriod?: '30d' | '90d' | 'all';
}

export interface CorrelationResult {
  id: string;
  feature1: string;
  feature2: string;
  dimension1: string;
  dimension2: string;
  /** Pearson / Spearman coefficient in [-1, +1]. */
  coefficient: number;
  /** Two-tailed p-value. */
  pValue: number;
  /** Composite confidence score in [0, 1]. */
  confidence: number;
  sampleSize: number;
  /** Lead time in days, null if contemporaneous. */
  lagDays: number | null;
  /** Granger causality assessment strength. */
  causalAssessment: 'suggestive' | 'moderate' | 'strong' | null;
  /** Human-readable narrative generated by the correlation engine. */
  narrative: string;
}

export interface UseCorrelationsReturn {
  correlations: CorrelationResult[];
  loading: boolean;
  error: string | null;
  /** Unique dimensions present in the result set. */
  availableDimensions: string[];
}

