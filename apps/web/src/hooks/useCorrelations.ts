// ─────────────────────────────────────────────────────────────────────────────
// Time period helpers
// ─────────────────────────────────────────────────────────────────────────────

function cutoffDate(period: '30d' | '90d' | 'all'): Date | null {
  if (period === 'all') return null;
  const d = new Date();
  d.setDate(d.getDate() - (period === '30d' ? 30 : 90));
  return d;
}

// ─────────────────────────────────────────────────────────────────────────────
// Hook implementation
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Fetches cross-dimension correlations and provides filtered, sorted results.
 *
 * Data source priority:
 * 1. `correlation_cache` Supabase table (real data)
 * 2. Realistic mock dataset (cold-start / demo)
 *
 * @param filters - Dimension, confidence, and time-period filters
 * @returns Filtered + sorted correlations with loading/error state
 */
export function useCorrelations(filters: CorrelationFilters): UseCorrelationsReturn {
  const [rawCorrelations, setRawCorrelations] = useState<CorrelationResult[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  const { dimensions = [], minConfidence = 0.5, timePeriod = '90d' } = filters;

  useEffect(() => {
    let cancelled = false;

    async function fetchCorrelations() {
      setLoading(true);
      setError(null);

      try {
        const supabase = createClient();
        const { data: { user } } = await supabase.auth.getUser();

        if (!user) {
          if (!cancelled) {
            setRawCorrelations(MOCK_CORRELATIONS);
            setLoading(false);
          }
          return;
        }

        // Build query against correlation_cache table
        let query = supabase
          .from('correlation_cache')
          .select(
            'id, feature1, feature2, dimension1, dimension2, coefficient, p_value, confidence, sample_size, lag_days, causal_assessment, narrative, computed_at',
          )
          .eq('user_id', user.id)
          .order('confidence', { ascending: false })
          .limit(200);

        // Apply server-side time filter when possible
        if (timePeriod !== 'all') {
          const cutoff = cutoffDate(timePeriod);
          if (cutoff) {
            query = query.gte('computed_at', cutoff.toISOString());
          }
        }

        const { data, error: queryError } = await query;

        if (cancelled) return;

        if (queryError) {
          // Table may not exist yet — fall back to mock data silently
          console.warn('[useCorrelations] Falling back to mock data:', queryError.message);
          setRawCorrelations(MOCK_CORRELATIONS);
        } else if (!data || data.length === 0) {
          // No real data yet — use mock data for a good cold-start experience
          setRawCorrelations(MOCK_CORRELATIONS);
        } else {
          // Map snake_case DB columns to camelCase interface
          const mapped: CorrelationResult[] = data.map((row) => ({
            id: row.id as string,
            feature1: row.feature1 as string,
            feature2: row.feature2 as string,
            dimension1: row.dimension1 as string,
            dimension2: row.dimension2 as string,
            coefficient: row.coefficient as number,
            pValue: row.p_value as number,
            confidence: row.confidence as number,
            sampleSize: row.sample_size as number,
            lagDays: row.lag_days as number | null,
            causalAssessment: row.causal_assessment as CorrelationResult['causalAssessment'],
            narrative: row.narrative as string,
          }));
          setRawCorrelations(mapped);
        }
      } catch (err) {
        if (!cancelled) {
          console.warn('[useCorrelations] Unexpected error:', err);
          setRawCorrelations(MOCK_CORRELATIONS);
          setError(null); // Don't surface errors when mock data covers it
        }
      } finally {
        if (!cancelled) setLoading(false);
      }
    }

    fetchCorrelations();
    return () => { cancelled = true; };
  }, [timePeriod]); // Re-fetch when time period changes

  // Client-side filtering and sorting
  const correlations = useMemo(() => {
    let result = [...rawCorrelations];

    // Filter by minimum confidence
    result = result.filter((c) => c.confidence >= minConfidence);

    // Filter by selected dimensions (either dimension1 or dimension2 must match)
    if (dimensions.length > 0) {
      result = result.filter(
        (c) => dimensions.includes(c.dimension1) || dimensions.includes(c.dimension2),
      );
    }

    // Sort by composite relevance: confidence * abs(coefficient)
    result.sort((a, b) => {
      const scoreA = a.confidence * Math.abs(a.coefficient);
      const scoreB = b.confidence * Math.abs(b.coefficient);
      return scoreB - scoreA;
    });

    return result;
  }, [rawCorrelations, dimensions, minConfidence]);

  // Derive unique dimensions from unfiltered raw data
  const availableDimensions = useMemo(() => {
    const dims = new Set<string>();
    rawCorrelations.forEach((c) => {
      dims.add(c.dimension1);
      dims.add(c.dimension2);
    });
    return Array.from(dims).sort();
  }, [rawCorrelations]);

  return { correlations, loading, error, availableDimensions };
}
